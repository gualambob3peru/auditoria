<?php


function getMonth($number)
{
    $array_month = ["Enero","Febrero","Marzo","Abril","Mayo","Junio","Julio","Agosto","Setiembre","Octubre","Noviembre","Diciembre"];

    return $array_month[$number-1];
}

function getMonthList()
{
    $array_month = ["Enero","Febrero","Marzo","Abril","Mayo","Junio","Julio","Agosto","Setiembre","Octubre","Noviembre","Diciembre"];

    return $array_month;
}
